function changeStatus(studentId, classId, targetDate) {
    debugger
    targetClassIndex = -1
    for(var i=0; i<classes.length; i++) {
        targetClassIndex = classes[i].class_id == classId ? i : targetClassIndex
    }

    targetStudentIndex = -1

    tmpStudents = classes[targetClassIndex].days[targetDate-1]
    for(var i=0; i<tmpStudents.length; i++) {
        targetStudentIndex = tmpStudents[i].student_id == studentId ? i : targetStudentIndex
    }
    targetStudentStatus = classes[targetClassIndex]["days"][targetDate-1][targetStudentIndex].status
    classes[targetClassIndex].days[targetDate-1][targetStudentIndex].status = targetStudentStatus == "attending" ? "absenting" : "attending"
}




